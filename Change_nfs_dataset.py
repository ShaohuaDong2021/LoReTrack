def read_and_write(base_path, name):
    print(name)

    path_for_read = base_path + '/' + name + '/' + '30' + '/' + name + '.txt'
    path_for_write = base_path + '/' + name + '/' + '30' + '/' + name + '_gt.txt'

    with open(path_for_read, 'r') as file:
        file_w = open(path_for_write, 'w')
        file_w.truncate(0)
        for line in file:
            line = line.strip().split()
            print(line)

            converted_data = [int(x) if i in range(1, 5) else x for i, x in enumerate(line)]
            line = [x for x in converted_data if isinstance(x, int)]

            print(line)


            x1 = int(line[0])
            y1 = int(line[1])
            x2 = int(line[2]) + 1
            y2 = int(line[3]) + 1
            w = x2 - x1
            h = y2 - y1

            file_w.write(str(x1) + ',')
            file_w.write(str(y1) + ',')
            file_w.write(str(w) + ',')
            file_w.write(str(h) + '\n')

        file_w.close()



